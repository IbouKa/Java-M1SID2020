package exercice4;

public class Point {
	public int x;
	public int y;
	
	public Point(int x,int y) {
		this.x=x;
		this.y=y;
	};
	
	//Definition des getters
	public int getX() {
		return this.x;
	};
	
	public int getY() {
		return this.y;
	};
	
	//Definition des setters
	public void setX(int x) {
		this.x=x;
	};
	public void setY(int y) {
		this.y=y;
	};

}
